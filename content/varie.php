<p>
Standard operations research and problem solving method assume that the
criterion to be optimized is given, and actually is a computable function
of some clearly defined input parameters. Unfortunately the real-world is
not a math classroom and defining the objectives of the problem solving /
optimization effort is the pain point which often consumes most resources,
time, and money. Try asking a manager &quot;Please tell me the function that
you are optimizing&quot;, responses may range from laughter to anger.
Recognizing this issue changes the computer science effort from improving
by 0.1% the results on artificially generated classroom problems to
devoting most computational resources to help a user learning more about
his problem, expressing his explicit and implicit preferences, identifying
methods, models and parameters in a semi-automated manner. You may call it
&quot;computer-supported consultancy&quot; if you want.
</p>
